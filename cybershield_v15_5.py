def detect_search_intent(keyword: str) -> str:
    keyword_lower = keyword.lower()
    
    tool_keywords = ['check', 'lookup', 'tool', 'checker', 'verify', 'test', 'ip', 'dns', 'whois',
                     'فحص', 'كشف', 'تحقق', 'أداة', 'اختبار', 'تتبع', 'باسورد', 'رابط', 'بريد']
    buy_keywords = ['اشتري', 'سعر', 'شراء', 'عرض', 'خصم', 'كوبون', 'coupon', 'discount', 'buy', 'price']
    compare_keywords = ['مقارنة', 'أفضل', 'بديل', 'vs', 'against', 'comparison', 'alternative', 'review']
    learn_keywords = ['كيف', 'ما هو', 'شرح', 'دليل', 'طريقة', 'تعلم', 'ماهو', 'ماهي', 'معنى', 'تعريف', 'لماذا', 'what is', 'how to', 'guide', 'tutorial']
    problem_keywords = ['مشكلة', 'خطأ', 'error', 'fix', 'حل', 'تجنب', 'problem', 'issue', 'not working']
    
    if any(tk in keyword_lower for tk in tool_keywords):
        return 'tool'
    if any(bk in keyword_lower for bk in buy_keywords):
        return 'transactional'
    if any(ck in keyword_lower for ck in compare_keywords):
        return 'commercial'
    if any(pk in keyword_lower for pk in problem_keywords):
        return 'problem_solving'
    if any(lk in keyword_lower for lk in learn_keywords):
        return 'informational'
    return 'tool'

