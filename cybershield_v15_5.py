class ZeroRepetitionTracker:
    def __init__(self):
        self.used_phrases = set()
        self.used_concepts = set()
        self.used_headings = set()
        self.used_examples = set()
        self.lock = threading.Lock()

    def consume(self, text: str, category: str = "general") -> bool:
        with self.lock:
            if category == "heading" and text in self.used_headings:
                return False
            if category == "example" and text in self.used_examples:
                return False
            if text in self.used_phrases:
                return False
            self.used_phrases.add(text)
            if category == "heading":
                self.used_headings.add(text)
            if category == "example":
                self.used_examples.add(text)
            return True

    def is_fresh(self, text: str) -> bool:
        return text not in self.used_phrases

    def clear(self):
        with self.lock:
            self.used_phrases.clear()
            self.used_concepts.clear()
            self.used_headings.clear()
            self.used_examples.clear()

    def get_used_count(self) -> int:
        with self.lock:
            return len(self.used_phrases)

tracker = ZeroRepetitionTracker()

